package com.kondratiev;

public class FloatingPointAndInt {


    public static void main(String[] args) {


        int a = 80;
        double b = 12.142;
        int c = 125;
        double d = 63.121;
        int e = 510;
        double f = 31.8612;

        System.out.println("a + b =" + (a + b));
        System.out.println("c * d =" + (c * d));
        System.out.println("e / f =" + (e / f));
    }
}