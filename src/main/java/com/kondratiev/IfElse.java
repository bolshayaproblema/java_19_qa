package com.kondratiev;

public class IfElse {

    public static void main(String[] args) {

        int a = 50;
        int b = 80;
        if (a > b) {

            System.out.println("a + b = " + (a + b));
        }
        else {
            System.out.println("b + a = " + (b + a));

        }
    }
}