package com.kondratiev;

public class Arithmetic {

    public static void main(String[] args) {
        int a = 23;
        int b = 38;
        int c = 54;
        int d = 86;

        System.out.println("a + b =" + (a + b));
        System.out.println("b - a =" + (a - b));
        System.out.println("a * b =" + (a * b));
        System.out.println("d / c =" + (d / c));
        System.out.println("d % c =" + (d % c));
        System.out.println("a % b =" + (a % b));

    }
}
